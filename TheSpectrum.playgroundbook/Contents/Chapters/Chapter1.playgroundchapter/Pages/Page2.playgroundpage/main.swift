# Why is there a gender bias?

Studies around Autism Spectrum Disorder (ASD) were conducted, _almost entirely_, on boys. Therefore the criteria for diagnosing ASD - a disorder mostly known for social and communication impairment and repetitive behavior - was based on how this disorder affected boys, _not girls_.

 ```
 Research into the experiences of girls with autism is limited, due partly to the predominance of male diagnoses, but also since the majority of studies focus on gender differences from  a  biogenic  viewpoint  or  using  quantitative  methodology.
 
 -(Autism – It’s Different in Girls - Applied Behavioral Consulting | ABA & Other Autism Therapies, 2021)
```
 
Doctors didn’t _purposefully_ leave girls out or assume they were incapable of being autistic, what happened was, doctors considered autism to be gender neutral. However, the symptoms for both boys and girls are completely different!

## When girls were diagnosed with ASD, they were usually extreme cases such as selective mutism, lower IQ, repetitive compulsive behavior, among other things, which in turn made doctors think that women were more severely affected by autism.


```
 “Experts also believed that girls with autism were, on average, more seriously affected —with more severe symptoms, such as intellectual disability. Newer research suggests that both these ideas may be wrong.”
 
    - (Autism – It’s Different in Girls - Applied Behavioral Consulting | ABA & Other Autism Therapies, 2021)
```
```
 “I heard [ at the doctors office] that I couldn't be autistic because I didn't have a fixation with dinosaurs.”
    - Sofie Ulsig
 ```

